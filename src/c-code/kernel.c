void shell()
{
	initialize_idt64();

	cls();
	printf("Welcome to\n%s", ascii_data);
	
	printf("\n\n[command] $ \n");

	char data[128] = "print hello%cworld @";
	char args[5][20] = {0};
	
	shell_parse(data, 128, args, 5);

	if (strcmp(args[0], "add") == 0)
	{
		add(atoi(args[1]), atoi(args[2]));
	}
	else if (strcmp(args[0], "print") == 0)
	{
		print(args[1], args);
	}
	else if (strcmp(args[0], "help") == 0)
		help();

	putc('\n');

}

void kernel_main(unsigned long magic, unsigned long addr) 
{
	cls();

	/* Make sure the magic number matches for memory mapping*/
	if(magic != MULTIBOOT2_BOOTLOADER_MAGIC) {
		puts("invalid magic number!");
		return;
	}

	int i = 0;
	multiboot_memory_map_t* ent;
	extract_mmap(addr);
	
	while ((ent = get_mmap_entery(i++)))
	{
		  printf (" base_addr = 0x%x%x, length = 0x%x%x, type = 0x%x\n",
		  (unsigned) (ent->addr >> 32),
		  (unsigned) (ent->addr & 0xffffffff),
		  (unsigned) (ent->len >> 32),
		  (unsigned) (ent->len & 0xffffffff),
		  (unsigned) ent->type);
	}
}
